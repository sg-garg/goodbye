<h2>Point of Contact</h2>
<p><strong>What is a Point of Contact</strong></p>
<p>A Point of Contact is a primary contact that will be able to verify your passing.  They will be notified by EGoodbyes when you list them here that they are a Point of Contact and will be able to submit evidence of you death.</p>
<p>EGoodbyes requires that your list at least <strong>  2 Points of Contact</strong> before we can release your personal goodbye messages or make public any memorials you may have created.</p>
